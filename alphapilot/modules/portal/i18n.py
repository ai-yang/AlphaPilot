def init_lang(default: str = "zh") -> None:
    if "portal_lang" not in st.session_state:
        st.session_state.portal_lang = default


def get_lang() -> str:
    return st.session_state.get("portal_lang", "zh")


def t(key: str, **kwargs: Any) -> str:
    lang = get_lang()
    text = TRANSLATIONS.get(lang, TRANSLATIONS["en"]).get(key) or TRANSLATIONS["en"][key]
    return text.format(**kwargs) if kwargs else text


def language_selector() -> None:
    st.sidebar.selectbox(
        t("sidebar_language"),
        options=["zh", "en"],
        format_func=lambda code: LANG_LABELS[code],
        key="portal_lang",
    )
